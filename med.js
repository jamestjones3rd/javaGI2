function months (x){
    x = prompt('Choose a number:')
    if(x == 1){
        return alert('January')
    }else if(x == 2){
        return alert('February')
    }else if(x == 3){
        return alert('March')
    }else if(x == 4){
        return alert('April')
    }else if(x == 5){
        return alert('May')
    }else if(x == 6){
        return alert('June')
    }else if(x == 7){
        return alert('July')
    }else if(x == 8){
        return alert('August')
    }else if(x == 9){
        return alert('September')
    }else if(x == 10){
        return alert('October')
    }else if(x == 11){
        return alert('November')
    }else if(x == 12){
        return alert('December')
    }else{
        return alert('INVALID NUMBER')
    }
}

months()

